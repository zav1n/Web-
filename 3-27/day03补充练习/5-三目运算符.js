/**----------------------------原题------------------------------------------
 * 三目运算符 将以下代码改写成三元表达式且输出结果
 */
let a = 12;
if (a >= 0 && a <= 20) {
    if (a % 2 === 0) {
        a += 2;
    }
} else {
    a -= 2;
}
console.log(a);

/**
 * ------------------------------三元表达式且输出结果---------------------------------------
 *                              表达式 (expr1) ? (expr2) : (expr3)
 */

let a = 12;
if (a >= 0 && a <= 20) {

    a % 2 === 0  ? a +=2 : a-=2
}
console.log(a);

