let n = [10, 20];     // new Array(10, 20)
let m = n;          //m[10,20]
let x = m;          //x[10,20]
m[0] = 100;         //m[100,20]
x = [30, 40];   // new Array[30, 40]
x[0] = 200;         //x[200,40]
m = x;              //m[200,40]   !!!X[1]报错 
m[1] = 300;         //m[200,300] 
n[2] = 400;         //n[10,20,400]

console.log(n);     //n[10,20,400]
console.log(m);     //m[200,300]
console.log(x);     //x[200,40]    这个错了  答案是[200,300]   因为m = x; 作用域是windows  所以内存地址修改成了windows